fn build_with_nust64(recipe: &GameRecipe, out_dir: &Path) -> Result<()> {
    let project_root = Path::new("n64game");
    let resources_dir = project_root.join("resources");
    if resources_dir.exists() {
        fs::remove_dir_all(&resources_dir)?;
    }
    fs::create_dir_all(&resources_dir)?;

    let source_data = out_dir.join("data");
    let source_assets = out_dir.join("assets");
    copy_dir_all(&source_data, &resources_dir.join("data"))?;
    copy_dir_all(&source_assets, &resources_dir.join("assets"))?;

    let status = Command::new("nust64")
        .current_dir(&project_root)
        .arg("run")
        .arg("--profile")
        .arg(&recipe.build.toolchain.profile)
        .arg("--output")
        .arg(out_dir.join(&recipe.build.rom_name))
        .status()
        .with_context(|| "Failed to invoke nust64")?;

    if !status.success() {
        anyhow::bail!("nust64 failed with status: {}", status);
    }

    Ok(())
}

fn build_with_n64chain(recipe: &GameRecipe, out_dir: &Path) -> Result<()> {
    let project_root = Path::new("n64libdragon");
    let fs_dir = project_root.join("filesystem");
    if fs_dir.exists() {
        fs::remove_dir_all(&fs_dir)?;
    }
    fs::create_dir_all(&fs_dir)?;

    let source_data = out_dir.join("data");
    let source_assets = out_dir.join("assets");
    copy_dir_all(&source_data, &fs_dir.join("data"))?;
    copy_dir_all(&source_assets, &fs_dir.join("assets"))?;

    let status = Command::new("libdragon")
        .arg("make")
        .current_dir(&project_root)
        .status()
        .with_context(|| "Failed to invoke libdragon make")?;

    if !status.success() {
        anyhow::bail!("libdragon make failed with status: {}", status);
    }

    let built_rom = project_root.join("build").join("game.z64");
    fs::copy(&built_rom, out_dir.join(&recipe.build.rom_name))
        .with_context(|| "Failed to copy built ROM")?;

    Ok(())
}

fn copy_dir_all(src: &Path, dst: &Path) -> Result<()> {
    fs::create_dir_all(dst)?;
    for entry in fs::read_dir(src)? {
        let entry = entry?;
        let file_type = entry.file_type()?;
        let src_path = entry.path();
        let dst_path = dst.join(entry.file_name());
        if file_type.is_dir() {
            copy_dir_all(&src_path, &dst_path)?;
        } else {
            fs::copy(&src_path, &dst_path)?;
        }
    }
    Ok(())
}
